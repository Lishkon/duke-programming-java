package week01.forEachLoops.Quiz;

public class Casting {
    public static void main(String[] args) {
        int m = 7 ;
        int n = 9 ;
        double d = 4.5 ;
        double f = 8.974 ;

        int y = (int)n + (int)f;
        double w = d + f;
        int x = m + n;
        double z = m + f;

        System.out.println(y);
        System.out.println(w);
        System.out.println(x);
        System.out.println(z);
    }
}
